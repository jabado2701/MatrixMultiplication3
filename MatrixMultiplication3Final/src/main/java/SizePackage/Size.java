package SizePackage;



public class Size {

    private Integer numRows;

    private Integer numColumns;


    public Size(Integer numRows, Integer numColumns) {
        this.numRows = numRows;
        this.numColumns = numColumns;
    }

    public Integer getNumRows() {
        return numRows;
    }

    public Integer getNumColumns() {
        return numColumns;
    }

}
